    }

    public override void Start()
    {
        profileTypes = new GameEnum.ProfileType[]
        {
            GameEnum.ProfileType.Light,
            GameEnum.ProfileType.Hero,
            GameEnum.ProfileType.Clown,
        };

        profileNames = new string[]
        {
            "그저 [빛]",
            "Hero",
            "광대"
        };
        
        DrawProfileText(currentIndex);
        DrawProfileImage();
    }

    public override void Update()
    {
        if (Console.KeyAvailable)
        {
            ConsoleKeyInfo input = Console.ReadKey(true);
            switch (input.Key)
            {
                case ConsoleKey.UpArrow:
                    if (currentIndex > 0)
                    {
                        currentIndex--;
                        DrawProfileText(currentIndex);
                        DrawProfileImage();
                    }
                    break;
                case ConsoleKey.DownArrow:
                    if (currentIndex < profileTypes.Length - 1)
                    {
                        currentIndex++;
                        DrawProfileText(currentIndex);
                        DrawProfileImage();
                    }
                    break;
                case ConsoleKey.Enter:
                    GameManager.Instance.PlayerData.ProfileType = profileTypes[currentIndex];
                    controller.ChangeScene<ClassScene>();
                    break;
            }
        }
    }

